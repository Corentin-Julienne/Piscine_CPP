#include "HumanA.hpp"
#include "HumanB.hpp"
#include "Weapon.hpp"

static int	verify_1(void)
{
	Weapon club = Weapon("crude spiked club");
	HumanA bob("Bob", club);
	bob.attack();
	club.setType("some other type of club");
	bob.attack();
	return (0);
}

static int	verify_2(void)
{
	Weapon club = Weapon("crude spiked club");
	HumanB jim("Jim");
	jim.setWeapon(club);
	jim.attack();
	club.setType("some other type of club");
	jim.attack();
	return (0);
}

int	main(void)
{
	verify_1();
	verify_2();
	return (0);
}
